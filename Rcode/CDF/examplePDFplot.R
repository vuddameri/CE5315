
x <- seq(-5,5,0.001)
fx <- dnorm(x,0,1)
Fx <- pnorm(x,0,1)
plot(x,fx,ylab='Density')
grid()
title('PDF')
abline(v=-1.96,col='red',lty=2,lwd=2)
plot(x,Fx,ylab='Probability')
grid()
title('CDF')
abline(v=-1.96,col='red',lty=2,lwd=2)
arrows(-1.95,0.0275,-5.275,0.025,col='red',lty=2,lwd=2,code=2,length=0.2)
integrate(dnorm,-Inf,-1.96)